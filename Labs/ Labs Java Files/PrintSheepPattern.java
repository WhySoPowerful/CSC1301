package labs;

public class PrintSheepPattern {
	public static void main(String[] args) {
		System.out.println("\t   '__'");
		System.out.println("\t   (oo)");
		System.out.println("\t/========//");
		System.out.println("\t/|| @@ ||");
		System.out.println("\t* ||--||");
		System.out.println("\t VV  VV ");
		System.out.println("\t \"  \"");
	}

}
